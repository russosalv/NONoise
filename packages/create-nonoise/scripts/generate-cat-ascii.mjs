#!/usr/bin/env node
/**
 * Generate ASCII-art version of the NONoise cat logo.
 *
 * Reads ../../logos/nonoise-cat.png, downscales it, and emits
 * src/cat-ascii.ts with CAT_MONO (plain ASCII) + CAT_COLOR (with ANSI
 * 256-color escapes derived from the source pixels).
 *
 * Run manually after updating the logo:
 *   node scripts/generate-cat-ascii.mjs
 *
 * The resulting src/cat-ascii.ts is committed to the repo so end-users
 * running `npx create-nonoise` don't need jimp installed.
 */
import { Jimp } from 'jimp';
import { writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const pkgRoot = resolve(here, '..');
const monoRoot = resolve(pkgRoot, '..', '..');
const SRC_PNG = resolve(monoRoot, 'logos', 'nonoise-cat.png');
const OUT_TS = resolve(pkgRoot, 'src', 'cat-ascii.ts');

// Target ASCII dimensions: we want the cat ~6 rows (matches the NONoise
// banner) and narrow enough to sit next to a 66-col wordmark on an 80-col
// terminal. Character cells are taller than wide (~2:1), so to keep aspect
// ratio correct for a roughly square source image we pick H=6, W=14.
const ASCII_H = 6;
const ASCII_W = 14;
// Character aspect compensation: each character cell is ~2x taller than it
// is wide, so when downscaling we stretch horizontally by 2x to preserve
// the cat's aspect ratio.
const CHAR_ASPECT = 2;

// Brightness ramp from darkest to lightest. Uses block characters for
// better fill density than the traditional ASCII ramp.
const RAMP = ' .:-=+*#%@';

function pickChar(brightness /* 0..1 */, alpha /* 0..1 */) {
  if (alpha < 0.35) return ' ';
  // Invert: darker pixels -> denser chars.
  const idx = Math.round((1 - brightness) * (RAMP.length - 1));
  return RAMP[Math.max(0, Math.min(RAMP.length - 1, idx))];
}

function rgbToAnsi256(r, g, b) {
  // Greyscale?
  if (r === g && g === b) {
    if (r < 8) return 16;
    if (r > 248) return 231;
    return Math.round(((r - 8) / 247) * 24) + 232;
  }
  const toCube = (v) => Math.round((v / 255) * 5);
  return 16 + 36 * toCube(r) + 6 * toCube(g) + toCube(b);
}

const image = await Jimp.read(SRC_PNG);
// Resize so that sampled cells correspond to (W * CHAR_ASPECT) x H pixel blocks,
// then sample at (W * CHAR_ASPECT, H) and map columns of CHAR_ASPECT pixels into
// a single character.
const sampleW = ASCII_W * CHAR_ASPECT;
const sampleH = ASCII_H;
image.resize({ w: sampleW, h: sampleH });

const monoLines = [];
const colorLines = [];

for (let y = 0; y < sampleH; y++) {
  let mono = '';
  let color = '';
  for (let cx = 0; cx < ASCII_W; cx++) {
    // Average the CHAR_ASPECT pixels that fall into this character cell.
    let rAcc = 0;
    let gAcc = 0;
    let bAcc = 0;
    let aAcc = 0;
    for (let dx = 0; dx < CHAR_ASPECT; dx++) {
      const px = cx * CHAR_ASPECT + dx;
      const { r, g, b, a } = image.getPixelColor
        ? intToRgba(image.getPixelColor(px, y))
        : readRgba(image, px, y);
      rAcc += r;
      gAcc += g;
      bAcc += b;
      aAcc += a;
    }
    const r = Math.round(rAcc / CHAR_ASPECT);
    const g = Math.round(gAcc / CHAR_ASPECT);
    const b = Math.round(bAcc / CHAR_ASPECT);
    const a = Math.round(aAcc / CHAR_ASPECT);
    const brightness = (0.299 * r + 0.587 * g + 0.114 * b) / 255;
    const alpha = a / 255;
    const ch = pickChar(brightness, alpha);
    mono += ch;
    if (ch === ' ') {
      color += ' ';
    } else {
      const code = rgbToAnsi256(r, g, b);
      color += `\x1b[38;5;${code}m${ch}`;
    }
  }
  // Reset at end of each colored line so stray codes don't leak.
  if (color.includes('\x1b')) color += '\x1b[0m';
  monoLines.push(mono);
  colorLines.push(color);
}

function intToRgba(int) {
  // Jimp 1.x stores packed RGBA; older versions expose a helper.
  return {
    r: (int >>> 24) & 0xff,
    g: (int >>> 16) & 0xff,
    b: (int >>> 8) & 0xff,
    a: int & 0xff,
  };
}

function readRgba(img, x, y) {
  const idx = (img.bitmap.width * y + x) * 4;
  const d = img.bitmap.data;
  return { r: d[idx], g: d[idx + 1], b: d[idx + 2], a: d[idx + 3] };
}

function toTsArray(arr) {
  return arr
    .map((l) => '  ' + JSON.stringify(l) + ',')
    .join('\n');
}

const header = `// AUTO-GENERATED by scripts/generate-cat-ascii.mjs — do not edit by hand.
// Source: logos/nonoise-cat.png
// Regenerate: node scripts/generate-cat-ascii.mjs
//
// CAT_MONO is plain ASCII (safe when NO_COLOR / non-TTY).
// CAT_COLOR embeds ANSI 256-color escapes approximating the logo's hues.
// Width: ${ASCII_W} chars, Height: ${ASCII_H} rows.
`;

const body = `export const CAT_MONO: readonly string[] = [
${toTsArray(monoLines)}
];

export const CAT_COLOR: readonly string[] = [
${toTsArray(colorLines)}
];

export const CAT_WIDTH = ${ASCII_W};
export const CAT_HEIGHT = ${ASCII_H};
`;

writeFileSync(OUT_TS, header + '\n' + body, 'utf8');
console.log(`Wrote ${OUT_TS}`);
console.log('\nPreview (mono):');
for (const l of monoLines) console.log('  |' + l + '|');
